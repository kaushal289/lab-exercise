'''5. For given integer x, print 'true' if it is positive, print'false' if it is negative
 and print'zero' if it is 0.'''
integer=int(input("enter a integer you like:"))
if integer>0:
    print("the number is positive")
    print("True")
elif integer<0:
    print("the number is negative")
    print("False")
else:
    print("zero")